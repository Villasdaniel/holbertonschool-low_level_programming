#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
/**
* op_add - addition of two numbers
* @a: number
* @b: number
* Return: int.
*/
int op_add(int a, int b) {return (a + b); }
/**
* op_sub - substraction of two numbers
* @a: Number
* @b: Number
* Return: int.
*/
int op_sub(int a, int b) {return (a - b); }
/**
* op_mul - multiply of two numbers
* @a: Number
* @b: Number
* Return: int.
*/
int op_mul(int a, int b) {return (a * b); }
/**
* op_div - division two numbers
* @a: number
* @b: number
* Return: int.
*/
int op_div(int a, int b) {return (a / b); }
/**
* op_mod - module of two numbers
* @a: number
* @b: number
* Return: int.
*/
int op_mod(int a, int b) {return (a % b); }